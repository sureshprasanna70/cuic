      <td></p>
          
          <br><p class="sub_head">Preamble: </p>
          <p class="content">The Centre for University - Industry Collaboration, Anna University Chennai was established in the year 1991 with a primary focus on the following domains namely Campus Placement for Engineering / Technology / Management Students, Interaction between University and Industry, Faculty Empowerment Programmes, Arranging Industrial Training, Industrial Visit and Industrial Project for Students.  Training Programmes for students on Core Competency Development and research programmes in engineering and technology.  The campus placement activities are regularly carried out every year for more than 4500 students belonging to UG / PG programmes studying in the University Departments. Every year around 200 companies from India and abroad, visit the academic portals of Anna University for their prestigious recruitment. The campus placement offers three kinds of jobs namely Core, IT and ITES for the students which cover 95% of the students from Anna University.  The CUIC also conducts "Tamil Nadu State Level Placement Programme" for the benefit of all the Engineering colleges affiliated to it.</p>
          
          <p class="sub_head">Industrial Associateship Scheme (IAS):</p>
          <p class="content">CUIC has a unique scheme of Industrial Associateship Scheme (IAS) wherein, small, medium, large scale industries, R&D and Educational institutions are enrolled with the main aim to carry out collaborative activities.<br>Presently, there are 150 Members from Industries, Engineering Colleges , Polytechnics and R & D Institutions. Various interactive Programmes are conducted every year for the benefit of the member-institutions. Programmes for promoting Industry-Institute interactions, Regional Get-togethers of industries, Government-funding agencies, R&D and Educational institutions, etc., are also organised at regular intervals for cross pollination of industrial / academic ideas.</p> 
          <p class="sub_head"> Benefits of Industrial Associateship Scheme (IAS): </p>
          <ul>
          <li> The members can avail 3 library tickets. Xeroxing of leading journal technical papers can be made and other related facilities shall be extended on a very nominal payment.</li>
          <li> Fifty percent concession on conslitancy charges will be given for the minimum charge alone. </li>
          <li>Twenty five percent concession on registration fee for attending Conferences / Short Term Courses / Continuing Education Programmes conducted by the Anna University , Chennai. </li>
          <li>Members will get preference in inviting the faclity of Anna University Chennai for any specific lecture / technical talk in their organization. </li>
         
          <li> General and technical information available in the CUIC will be provided to the members and the exchange of information shall be both ways. </li>
          <li> Priority will be given by the University in taking up student projects and industrial training in areas identified by the member industries with their cooperation and financial support. </li>
          <li> Industry members can avail of / may provide video films / information materials on products manufacturing techniques etc., </li>
          <li> Annual Publication, calendar, directory, brochures etc., will be sent exclusively to the members. </li>
          </ul>
          <p class="sub_head">Industrial Visits/Industrial Training/Internship</p>
          <p class="content">
          <ul>
            <li>Industrial training has been made mandatory for all Under-Graduate Branches of Engineering as per the curriculum. This gives wide exposure about the various functions of the Industries prior to the completion of their studies. CUIC with assistance from various industries and departments is arranging Industrial Visits, Inplant Training and Internship to students of Anna University.</li>
            <li>Anna University Chennai is offering three 5 Years Integrated Courses namely M.Sc. Computer Science, M.Sc. Information Technology and M.Sc. Electronic Media Science for the students of +2 level (HSC). Those students have to do an intensive one semester intern project in the industries.  In this regard, the CUIC plays a vital role in identifying Core / IT Companies to provide students level to innovative application oriented projects from real life situation.</li>
            </ul>
            </p>
            <p class="sub_head">Memorandum of Understanding: </p>
            <p class="content">The CUIC has also been instrumental in forging many MoUs with various Industries and Research and Development Establishments. These MoUs will enable the students to get Internship training, Placement, Industrial Readiness, Professional Competencies and also Soft Skills Development.</p>
            <p class="sub_head">MoU with Tata Consultancy Services:</p>
            <p class="content">A MoU has signed with Tata Consultancy Services to establish Academic Interface Programme at Anna University.  Under this programme, Tata Consultancy Services is extending his support to organize Workshop for students, Faculty Development Programs for teachers, Best Student Awards and Best Project Award to encourage healthy competition at Colleges, Internship Training opportunity for students.</p>
            <p class="sub_head">MoU with Accenture Services Private Limited</p>
            <p class="content">M/s Accenture has signed a MoU to create Anna Accenture Innovation Laboratory at CUIC premises.  Based on this MoU, Accenture has donated 50 Computers and one Server to set  up the Innovation Laboratory.</p>
            <p class="sub_head">Tamil Nadu State Level Placement Programme (TNSLPP): </p>
            <p class="content">The CUIC not only concentrates on placement of the students of University Departments of Anna University but also that for the students of the constituent colleges, Government Colleges, Aided Colleges and Affiliated Colleges under Anna University through Tamil Nadu State Level Placement Programme (TNSLPP) by requesting the IT and Core and other industries to create confidence to all students. 
            </p>
